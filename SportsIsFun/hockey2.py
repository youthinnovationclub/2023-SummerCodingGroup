import turtle
import time
screen = turtle.Screen()
screen.bgcolor("white")
tur = turtle.Turtle()
tur.hideturtle()
while True:
    def move(t, x, y, a, sx):
        turtle.tracer(0,0)
        t.color('#B1F4E7')
        t.penup()
        t.goto(-400, -127)
        t.pendown()
        t.begin_fill()
        for g in range(4):
            t.forward(800)
            t.right(90)
        t.end_fill()
        t.color('#000000')
        t.width(1)
        t.penup()
        t.goto(x, y)
        t.pendown()
        t.begin_fill()
        t.circle(10)
        t.end_fill()
        t.penup()
        t.goto(sx, 90) 
        t.pendown()
        t.right(a)
        t.color('#744f25')
        t.width(4)
        t.forward(170)
        t.begin_fill()
        for x in range(2):
            t.forward(70)
            t.right(90)
            t.forward(20)
            t.right(90)
        t.end_fill()
        turtle.update()
        t.setheading(0)
        time.sleep(0.1)
        t.clear()
    move(tur, -30, -125, 30, 135)
    move(tur, -30, -125, 40, 110)
    move(tur, -30, -125, 45, 80)
    move(tur, -30, -125, 60, 50)
    move(tur, -30, -125, 90, 20)
    move(tur, -100, -115, 110, 0)
    move(tur, -160, -105, 130, 0)
    move(tur, -210, -95, 130, 0)
    move(tur, -280, -90, 130, 0)
    move(tur, -340, -85, 130, 0)
    
    
